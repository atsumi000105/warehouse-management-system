<template>
    <section class="content">
        <div class="pull-right">
            <div class="btn-group">
                <button
                    class="btn btn-info btn-flat dropdown-toggle"
                    data-toggle="dropdown"
                >
                    <i class="fa fa-info-circle" /> {{ client.status | statusFormat }}
                </button>
                <ul
                    v-if="client.workflow.enabledTransitions"
                    class="dropdown-menu dropdown-menu-right"
                >
                    <li
                        v-for="enabledTransition in client.workflow.enabledTransitions.sort()"
                        :key="enabledTransition.transition"
                    >
                        <a
                            @click.prevent="doTransition(enabledTransition.transition)"
                        >
                            <i class="fa fa-arrow-circle-right" />{{ enabledTransition.title }}
                        </a>
                    </li>
                </ul>
            </div>
            <button
                class="btn btn-success btn-flat"
                @click.prevent="save"
            >
                <i class="fa fa-save fa-fw" />
                Save Client
            </button>
            <div class="btn-group">
                <button
                    type="button"
                    class="btn btn-default dropdown-toggle dropdown btn-flat"
                    data-toggle="dropdown"
                >
                    <span class="fa fa-ellipsis-v" />
                </button>
                <ul class="dropdown-menu dropdown-menu-right">
                    <li>
                        <a
                            href="#"
                            @click.prevent="askDelete"
                        >
                            <i class="fa fa-trash fa-fw" />Delete Client</a>
                    </li>
                </ul>
            </div>
        </div>
        <h3 class="box-title">
            Edit Client
        </h3>

        <div class="row">
            <form role="form">
                <div class="col-md-12">
                    <div class="box box-info">
                        <div class="box-header with-border">
                            <h3 class="box-title">
                                <i class="icon fa fa-group fa-fw" />Client Info
                            </h3>
                        </div>
                        <!-- /.box-header -->
                        <div class="box-body">
                            <div class="nav-tabs-custom">
                                <ul class="nav nav-tabs">
                                    <li class="active">
                                        <a
                                            href="#client_info"
                                            data-toggle="tab"
                                            aria-expanded="true"
                                        >Client Information</a>
                                    </li>
                                    <li>
                                        <a
                                            href="#profile_tab"
                                            data-toggle="tab"
                                        >Client Profile</a>
                                    </li>
                                    <li>
                                        <a
                                            href="#distribution_history"
                                            data-toggle="tab"
                                        >Distribution History</a>
                                    </li>
                                </ul>
                                <div class="tab-content">
                                    <div id="client_info" class="row tab-pane active">
                                        <div class="col-md-6">
                                            <div class="form-group">
                                                <label>First Name</label>
                                                <input
                                                    v-model="client.firstName"
                                                    type="text"
                                                    class="form-control"
                                                    placeholder="Enter first name"
                                                >

                                                <label>Last Name</label>
                                                <input
                                                    v-model="client.lastName"
                                                    type="text"
                                                    class="form-control"
                                                    placeholder="Enter last name"
                                                >
                                            </div>
                                            <div class="form-group">
                                                <label>Parent/Guardian First Name</label>
                                                <input
                                                    v-model="client.parentFirstName"
                                                    type="text"
                                                    class="form-control"
                                                    placeholder="Enter parent or guardian first name"
                                                >

                                                <label>Parent/GuardianLast Name</label>
                                                <input
                                                    v-model="client.parentLastName"
                                                    type="text"
                                                    class="form-control"
                                                    placeholder="Enter parent or guarding last name"
                                                >
                                            </div>
                                            <DateField
                                                v-model="client.birthdate"
                                                label="Birthdate"
                                                format="YYYY-MM-DD"
                                            />
                                            <PartnerSelectionForm
                                                v-model="client.partner"
                                                label="Assigned Partner"
                                            />
                                        </div>
                                        <div class="col-md-6">
                                            <div class="box box-info">
                                                <div class="box-header with-border">
                                                    <h3 class="box-title">
                                                        <i class="icon fa fa-clock-o fa-fw" />Expiration Info
                                                    </h3>
                                                </div>
                                                <!-- /.box-header -->
                                                <div class="box-body">
                                                    <!-- text input -->
                                                    <BooleanField
                                                        v-model="client.isExpirationOverridden"
                                                        label="Override Expirations"
                                                    />
                                                    <DateField
                                                        v-model="client.ageExpiresAt"
                                                        label="Age Expiration"
                                                        format="YYYY-MM-DD"
                                                    />
                                                    <DateField
                                                        v-model="client.distributionExpiresAt"
                                                        label="Distribution Expiration"
                                                        format="YYYY-MM-DD"
                                                    />
                                                    <NumberField
                                                        v-model="client.pullupDistributionMax"
                                                        label="Pullup Maximum Limit"
                                                    />
                                                    <DisplayField
                                                        v-model="client.pullupDistributionCount"
                                                        label="Pullup Distributions"
                                                    />
                                                </div>
                                                <!-- /.box-body -->
                                            </div>
                                        </div>
                                    </div>
                                    <AttributesEditForm
                                        id="profile_tab"
                                        v-model="client.attributes"
                                        class="tab-pane"
                                    />
                                    <ClientDistributionHistory
                                        id="distribution_history"
                                        :client="client"
                                        class="tab-pane"
                                    />
                                </div>
                            </div>
                            <!-- text input -->

                        </div>
                        <!-- /.box-body -->
                    </div>
                </div>

            </form>
        </div>

        <modal
            id="confirmModal"
            :confirm-action="deleteClient"
            classes="modal-danger"
        >
            <template slot="header">
                Delete Client
            </template>
            <p>Are you sure you want to delete <strong>{{ client.firstName }} {{ client.lastName }}</strong>?</p>
            <template slot="confirmButton">
                Delete Client
            </template>
        </modal>
    </section>
</template>

<script>
import Modal from '../../components/Modal.vue';
import AttributesEditForm from "../../components/AttributesEditForm";
import PartnerSelectionForm from "../../components/PartnerSelectionForm";
import DateField from "../../components/DateField";
import BooleanField from "../../components/ToggleField";
import NumberField from "../../components/NumberField";
import DisplayField from "../../components/DisplayField";
import ClientDistributionHistory from "./ClientDistributionHistory";

export default {
        name: 'ClientEdit',
        components: {
            ClientDistributionHistory,
            DisplayField,
            NumberField,
            BooleanField,
            DateField,
            PartnerSelectionForm,
            AttributesEditForm,
            'modal' : Modal
        },
        props: {
            new: {
                type: Boolean,
                default: false,
                required: false
            }

        },
        data() {
            return {
                client: {
                    partner: {},
                    attributes: [],
                    status: '',
                    workflow: {},
                },
                transition: '',
            };
        },
        created() {
            let self = this;

            if (!this.new) {
                axios
                    .get('/api/clients/' + this.$route.params.id, {
                        params: { include: ['partner', 'attributes']}
                    })
                    .then(response => {
                        self.client = response.data.data;
                        self.client.workflow = response.data.meta;
                    })
                    .catch(error => console.log("Error receiving clients %o", error));
            }

            console.log('ClientEdit Component mounted.');
        },
        methods: {
            save: function () {
                let self = this;
                if (this.new) {
                    axios
                        .post('/api/clients', this.client)
                        .then(response => self.$router.push({ name: 'clients' }))
                        .catch(function (error) {
                            console.log("Save this.client error %o", error);
                        });
                } else {
                    axios
                        .patch('/api/clients/' + this.$route.params.id, this.client)
                        .then(response => self.$router.push({ name: 'clients' }))
                        .catch(function (error) {
                            console.log("Save this.client error with params id %o", error);
                        });
                }
            },
            doTransition: function(transition) {
                let self = this;
                axios.patch('/api/clients/' + this.$route.params.id + '/transition', {'transition': transition})
                    .then(response => {
                        self.client.workflow = response.data.meta;
                        self.client.status = response.data.data.status;
                    });
            },
            askDelete: function() {
                $('#confirmModal').modal('show');
            },
            deleteClient: function() {
                let self = this;
                axios
                    .delete('/api/clients/' + this.$route.params.id)
                    .then(response => self.$router.push({ name: 'clients' }));
            }
        }
    }
</script>

